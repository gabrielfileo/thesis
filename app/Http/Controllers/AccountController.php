    public function editPassword(Request $request)
    {
        $entity = User::find(Auth::user()->id);
        $entity->name = $request->input('fullName');
        $entity->password = Hash::make($request->input('new_password'));
        $entity->save();
        Auth::logout();
        return view('auth.login');
    }

